que = queue.Queue()
que.put([0,0]) # starting position
marked = set()
marked.add((0,0))
parent = {}
while not que.empty():
    val = que.get()
    x = val[0]
    y = val[1]
    current_val = matrix[y][x]

    #Right
    if x+1 < len(matrix[y]):
        if abs(matrix[y][x+1]-current_val)<=10:
            if not (x+1,y) in marked:
                parent[(x+1,y)] = val
                marked.add((x+1,y))
                que.put([x+1,y])
    #Down
    if y+1 < len(matrix):
        if abs(matrix[y+1][x]-current_val) <= 10:
            if not (x,y+1) in marked:
                parent[(x, y+1)] = val
                marked.add((x,y+1))
                que.put([x,y+1])
    #Up
    if y-1 >= 0:
        if abs(matrix[y-1][x]-current_val) <=10:
            if not (x,y-1) in marked:
                parent[(x,y-1)] = val
                marked.add((x,y-1))
                que.put([x,y-1])
    #Left
    if x-1 >= 0:
        if abs(matrix[y][x-1]-current_val) <= 10:
            if not (x-1,y) in marked:
                parent[(x-1,y)] = val
                marked.add((x-1,y))
                que.put([x-1,y])

